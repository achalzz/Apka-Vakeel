// Helper function to search rights by keyword
export const searchRights = (keyword) => {
  const results = []
  const searchTerm = keyword.toLowerCase()
  
  Object.values(INDIAN_RIGHTS).forEach(category => {
    if (category.rights) {
      category.rights.forEach(right => {
        if (
          right.title?.toLowerCase().includes(searchTerm) ||
          right.description?.toLowerCase().includes(searchTerm) ||
          right.article?.toLowerCase().includes(searchTerm) ||
          right.details?.some(detail => detail.toLowerCase().includes(searchTerm))
        ) {
          results.push({ ...right, category: category.title })
        }
      })
    }
    if (category.principles) {
      category.principles.forEach(principle => {
        if (
          principle.title?.toLowerCase().includes(searchTerm) ||
          principle.description?.toLowerCase().includes(searchTerm)
        ) {
          results.push({ ...principle, category: category.title })
        }
      })
    }
  })
  
  return results
}

// Get all rights in a flat structure
export const getAllRights = () => {
  const allRights = []
  Object.values(INDIAN_RIGHTS).forEach(category => {
    if (category.rights) {
      category.rights.forEach(right => {
        allRights.push({ ...right, category: category.title })
      })
    }
    if (category.principles) {
      category.principles.forEach(principle => {
        allRights.push({ ...principle, category: category.title })
      })
    }
  })
  return allRights
}

